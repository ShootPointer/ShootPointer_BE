	public static List<PostData> generateRandomPosts(int size) {
		List<PostData> postDataList = new ArrayList<>();
		
		for (int i = 0; i < size; i++) {
			String title = TITLE_TEMPLATES[i % TITLE_TEMPLATES.length];
			String content = CONTENT_TEMPLATES[i % CONTENT_TEMPLATES.length];
			long likeCnt = random.nextInt(0, 100); // 0~99 랜덤 좋아요 개수
			
			postDataList.add(new PostData(title, content, likeCnt, (long) (i + 1)));
		}
		
		return postDataList;
	}
	
	/**
	 * 게시물 데이터를 담는 내부 클래스
	 */
	public static class PostData {
		private final String title;
		private final String content;
		private final Long likeCnt;
		private final Long postId;
		
		public PostData(String title, String content, Long likeCnt, Long postId) {
			this.title = title;
			this.content = content;
			this.likeCnt = likeCnt;
			this.postId = postId;
		}
		
		public String getTitle() {
			return title;
		}
		
		public String getContent() {
			return content;
		}
		
		public Long getLikeCnt() {
			return likeCnt;
		}
		
		public Long getPostId() {
			return postId;
		}
	}
